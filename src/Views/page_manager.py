import tkinter as tk
from tkinter import font as tkfont
from src.Views.start_page import StartPage
from src.Views.maintenance_page import Maintenance_Page
from src.Views.motorcycle_page import Motorcycle_Page
from src.Views.delete_motorcycle import Delete_Motorcycle_Page
from src.Views.update_motorcycle_page import Update_Motorcycle_Page

class Motorcycle_Maintenance(tk.Tk):
    def __init__(self, *args, **kwargs):
        tk.Tk.__init__(self, *args, **kwargs)

        self.title_font = tkfont.Font(family='Helvetica', size=18, weight="bold", slant="italic")

        # the container is where we'll stack a bunch of frames
        # on top of each other, then the one we want visible
        # will be raised above the others
        container = tk.Frame(self)
        container.pack(side="top", fill="both", expand=True)
        container.grid_rowconfigure(0, weight=1)
        container.grid_columnconfigure(0, weight=1)

        self.frames = {}
        for F in (StartPage, Motorcycle_Page, Delete_Motorcycle_Page, Update_Motorcycle_Page, Maintenance_Page):
            page_name = F.__name__
            frame = F(parent=container, controller=self)
            self.frames[page_name] = frame

            # put all of the pages in the same location;
            # the one on the top of the stacking order
            # will be the one that is visible.
            frame.grid(row=0, column=0, sticky="nsew")

        self.show_frame("StartPage")

    def show_frame(self, page_name):
        frame = self.frames[page_name]
        frame.tkraise()
